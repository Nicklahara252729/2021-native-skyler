$(function(){
    $('#produk').click(function(){
        $('#input-barang').css({
            display:'block',
        });
        $('#left-side').css({
            display:'none',
        });
        $('#main').css({
            display:'none',
        });
    });
    $('.li-setting').click(function(){
        $('.setting').slideDown();
        $('.bantuan').slideUp();
        $('.user').slideUp();
    });
    $('.li-bantuan').click(function(){
        $('.setting').slideUp();
        $('.bantuan').slideDown();
        $('.user').slideUp();
    });
    $('.li-user').click(function(){
        $('.setting').slideUp();
        $('.bantuan').slideUp();
        $('.user').slideDown();
    });
    $('.user').mouseenter(function(){
        $('.user').css({
            display:'block',
        });
    });
    $('.user').mouseleave(function(){
        $('.user').slideUp();
    });
    $('.setting').mouseenter(function(){
        $('.setting').css({
            display:'block',
        });
    });
    $('.setting').mouseleave(function(){
        $('.setting').slideUp();
    });
});